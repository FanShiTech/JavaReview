
public class Pizza {
	
	String bread;
	String sauce;
	String cheese;
	String topping;
	int size;
	
	Pizza(String bread, String sauce, String cheese, String topping){
		
		this.bread = bread;
		this.sauce = sauce;
		this.cheese = cheese;
		this.topping = topping;
		
	}
	
	Pizza(int size){
		this.size = size;
		
	}

}
